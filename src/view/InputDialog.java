    public String[] getColours() {
        FoodColourEnum [] colourEnums = FoodColourEnum.values();
        String colours [] = new String[colourEnums.length];
        for(int i = 0; i < colours.length; i++) {
            colours[i] = colourEnums[i].getDisplayedColourName();
        }
        return colours;
    }
    
