    public DefaultMutableTreeNode createNodes() {
        DefaultMutableTreeNode topNode = new DefaultMutableTreeNode("Tags");
        DefaultMutableTreeNode childNode = null;
        for(FoodTagEnum tag : labels) {
            childNode = new DefaultMutableTreeNode(tag.getDisplayedName());
            topNode.add(childNode);
        }
        return topNode;
    }
    
    public DefaultMutableTreeNode getAvailableNodes() {
        DefaultMutableTreeNode topNode = new DefaultMutableTreeNode("Tags");
        DefaultMutableTreeNode childNode = null;
        FoodTagEnum[] tagsArray = FoodTagEnum.values();
        List<String> tagsList = new ArrayList<>();
        for(FoodTagEnum tag : tagsArray) {
            tagsList.add(tag.getDisplayedName());
        }

        for (FoodTagEnum tag : labels) {
                tagsList.remove(tag.getDisplayedName());
        }
        
        for (String tag : tagsList) {
            childNode = new DefaultMutableTreeNode(tag);
            topNode.add(childNode);
        }
        return topNode;
    }
    
